package org.apache.spark.examples.tpch

import org.apache.spark.sql.functions._

class TPCHQ4(path: String) extends AbstractTPCHQuery(path) {

    spark.conf.set("appName", "TPCHQ4 : " + path)

  def submit() {
    import spark.implicits._

    val forders = order.filter($"o_orderdate" >= "1993-07-01" && $"o_orderdate" < "1993-10-01")
    val flineitems = lineitem.filter($"l_commitdate" < $"l_receiptdate")
      .select($"l_orderkey")
      .distinct

    val res = flineitems.join(forders, $"l_orderkey" === forders("o_orderkey"))
      .groupBy($"o_orderpriority")
      .agg(count($"o_orderpriority"))
      .sort($"o_orderpriority")

    try { res.collect } catch { case e: Exception => e.printStackTrace }
  }

}
